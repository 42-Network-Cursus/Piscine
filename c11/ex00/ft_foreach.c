void    ft(int n)
{
    n = n + 1;
}

void	ft_foreach(int *tab, int length, void (*ptf)(int))
{
	int i;
    int j;

	i = 0;
	while (i < length)
	{
		j = ptf(tab[i]);
		i++;
	}
}
